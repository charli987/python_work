# integers are whole numbers
#adding integers
print(3 + 2)
#subtracting integers
print(3 - 2)
#multiplying integers
print(3 * 2)
#dividing integers
print(3 / 2)
#integers to the power of
print(3 ** 2)

#floats are numbers with a decimal point
#adding floats
print(0.6 + 0.2)
#subtracting floats
print(0.6 - 0.2)
#multiplying floats
print(0.6 * 0.2)
#dividing floats
print(0.6 / 0.2)
#floats to the power of
print(0.6 ** 0.2)

#when using integers and floats in an equation, the answer will always be a float
#the answer to any division is always given as a float
print(1 + 2.0)
print(1.5 * 2)

